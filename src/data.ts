    function playZenClack() {
      if (isMuted) return;
      try {
        const audioCtx = new (window.AudioContext || window.webkitAudioContext)();
        const now = audioCtx.currentTime;
        for (let i = 0; i < 4; i++) {
          const startTime = now + i * 0.09;
          const osc = audioCtx.createOscillator();
          const gainNode = audioCtx.createGain();
          osc.type = "sine";
          osc.frequency.setValueAtTime(550 + Math.random() * 350, startTime);
          osc.frequency.exponentialRampToValueAtTime(120, startTime + 0.11);
          gainNode.gain.setValueAtTime(0.12, startTime);
          gainNode.gain.exponentialRampToValueAtTime(0.001, startTime + 0.11);
          osc.connect(gainNode);
          gainNode.connect(audioCtx.destination);
          osc.start(startTime);
          osc.stop(startTime + 0.13);
        }
      } catch (err) {
        console.warn('Audio err:', err);
      }
    }

