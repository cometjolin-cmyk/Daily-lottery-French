    // --- 動態生成 46 個捲軸，並塞入玻璃球 ---
    function initScrolls() {
      let seed = 98765;
      function pseudoRandom() {
        const x = Math.sin(seed++) * 10000;
        return x - Math.floor(x);
      }

      const layers = [
        { yMin: 62, yMax: 82, count: 14, xSpan: 76 },
        { yMin: 40, yMax: 62, count: 12, xSpan: 68 },
        { yMin: 18, yMax: 40, count: 10, xSpan: 54 },
        { yMin: -4, yMax: 18, count: 7, xSpan: 38 },
        { yMin: -24, yMax: -4, count: 4, xSpan: 18 },
      ];

      const pileArr = [];
      layers.forEach((layer) => {
        for (let i = 0; i < layer.count; i++) {
          const fraction = layer.count > 1 ? i / (layer.count - 1) : 0.5;
          const baseX = -layer.xSpan + fraction * 2 * layer.xSpan;
          const xJitter = (pseudoRandom() - 0.5) * 8;
          const x = baseX + xJitter;
          const y = layer.yMin + pseudoRandom() * (layer.yMax - layer.yMin);
          const rotate = -85 + pseudoRandom() * 170;
          const colorType = pseudoRandom() > 0.45 ? 'pink' : 'white';
          const scale = 0.82 + pseudoRandom() * 0.22;

          pileArr.push({ x, y, rotate, colorType, scale });
        }
      });

      pileArr.sort((a, b) => a.y - b.y);

      pileArr.forEach((sc) => {
        const div = document.createElement('div');
        const leftPos = 128 + sc.x - 16;
        const topPos = 128 + sc.y - 6;
        
        div.style.left = leftPos + 'px';
        div.style.top = topPos + 'px';
        div.style.transform = 'rotate(' + sc.rotate + 'deg) scale(' + sc.scale + ')';
        div.style.setProperty('--rot', sc.rotate + 'deg');
        
        div.className = 'absolute w-8 h-3 rounded-full border border-black/15 shadow-[0_1px_3px_rgba(0,0,0,0.2)] transition-all duration-300 z-10 scroll-node';
        if (sc.colorType === 'pink') {
          div.className += ' bg-gradient-to-r from-[#FFD2CC] via-[#FFA899] to-[#FFC4BA]';
        } else {
          div.className += ' bg-gradient-to-r from-[#FFFDFD] via-[#F4E3E0] to-[#FFFDFD]';
        }

        // 紙捲紋理
        const curlLeft = document.createElement('div');
        curlLeft.className = 'absolute inset-y-0 left-0.5 w-[2px] bg-black/10 rounded-full';
        const curlRight = document.createElement('div');
        curlRight.className = 'absolute inset-y-0 right-0.5 w-[2px] bg-black/10 rounded-full';
        const ribbon = document.createElement('div');
        ribbon.className = 'absolute inset-y-0 left-1/2 w-[2px] -translate-x-1/2 ' + (sc.colorType === 'pink' ? 'bg-white/60' : 'bg-red-400/80');

        div.appendChild(curlLeft);
        div.appendChild(curlRight);
        div.appendChild(ribbon);
        scrollsPile.appendChild(div);
      });
    }

