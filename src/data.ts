    async function loadGoogleSheetsData() {
      if (!SPREADSHEET_ID || SPREADSHEET_ID === 'YOUR_GOOGLE_SHEET_ID_HERE') {
        return;
      }
      const sheetUrl = "https://opensheet.elk.sh/" + SPREADSHEET_ID + "/工作表1";
      try {
        const response = await fetch(sheetUrl);
        if (!response.ok) throw new Error('Sheet network fail');
        const data = await response.json();
        if (Array.isArray(data) && data.length > 0) {
          const parsedData = data.map((item, index) => {
            const defaultMatch = DEFAULT_CHITS[index % DEFAULT_CHITS.length];
            return {
              id: item.id || String(index + 1),
              image_url: item.image_url || defaultMatch.image_url,
              french: item.french || defaultMatch.french,
              chinese: item.chinese || defaultMatch.chinese,
              interpretation: item.interpretation || defaultMatch.interpretation
            };
          });
          currentChits = parsedData;
        }
      } catch (err) {
        console.warn('Google Sheets loading fallback:', err);
      }
    }

